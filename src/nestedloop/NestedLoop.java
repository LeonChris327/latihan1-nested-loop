package nestedloop;
public class NestedLoop {

    
    
    public static void main(String[] args) {
        
    for(int b=1; b<=4; b++){
     
     for(int c=1; c<=b; c++){
     
     System.out.print("*");
     }
     System.out.println(" ");   
     } 
    
     
     for(int a=0; a<5; a++){
     
     for(int i=a; i<5; i++){
     
     System.out.print("*");
     }
     System.out.println(" ");   
     }
     
     
     }
       
  
    
        

    
    }
    